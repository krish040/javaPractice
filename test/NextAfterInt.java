import java.util.Scanner;

public class NextAfterInt {
    public static void main(String[] args) {

        Scanner scan=new Scanner(System.in);

        int n=scan.nextInt();
        int s=scan.nextInt();
        //String s=scan.nextLine();
        
        System.out.println(n);
        System.out.println(s);
        
        scan.close();
    }
}
